package com.hwadee.zgs.pojo;

public class Addlist {
	private int addlist_id;
	private int list_user_id;
    private String telenum;
    private String email;
    private String userName;
	
    public int getAddlist_id() {
		return addlist_id;
	}
	public void setAddlist_id(int addlist_id) {
		this.addlist_id = addlist_id;
	}
	public int getList_user_id() {
		return list_user_id;
	}
	public void setList_user_id(int user_list_id) {
		this.list_user_id = user_list_id;
	}
	public String getTelenum() {
		return telenum;
	}
	public void setTelenum(String telenum) {
		this.telenum = telenum;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
}